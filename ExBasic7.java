package variables;

public class ExBasic7 {

	public static void main(String[] args) {
		int radius = 3;
		double PI = 3.14;
		int heightRectangle = 4;
		int widthRectangle = 5;

		double circleS = (radius * radius) * PI;
		double rectangleS = heightRectangle * widthRectangle;

		System.out.println(circleS);
		System.out.println(rectangleS);
	}

}
